print("\nPrinting numbers from 0 to 99.")
for numbers in range(0, 100):
    print(numbers)

print("\nPrinting the even numbers from 0 to 99.")
for n in range(0, 100):
    if n % 2 == 0:
        print(n)

print("\nPrinting the odd numbers from 0 to 99.")
for num in range(0, 100):
    if n % 2 == 1:
        print(num)